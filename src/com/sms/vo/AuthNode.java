package com.sms.vo;

public class AuthNode
{
	int node_id;
	String node_name;
	String title;
	int node_status;
	String node_remark;
	int sort;
	int node_parent_id;
	int node_level;
	public int getNode_id()
	{
		return node_id;
	}
	public void setNode_id(int node_id)
	{
		this.node_id = node_id;
	}
	public String getNode_name()
	{
		return node_name;
	}
	public void setNode_name(String node_name)
	{
		this.node_name = node_name;
	}
	public String getTitle()
	{
		return title;
	}
	public void setTitle(String title)
	{
		this.title = title;
	}
	public int getNode_status()
	{
		return node_status;
	}
	public void setNode_status(int node_status)
	{
		this.node_status = node_status;
	}
	public String getNode_remark()
	{
		return node_remark;
	}
	public void setNode_remark(String node_remark)
	{
		this.node_remark = node_remark;
	}
	public int getSort()
	{
		return sort;
	}
	public void setSort(int sort)
	{
		this.sort = sort;
	}
	public int getNode_parent_id()
	{
		return node_parent_id;
	}
	public void setNode_parent_id(int node_parent_id)
	{
		this.node_parent_id = node_parent_id;
	}
	public int getNode_level()
	{
		return node_level;
	}
	public void setNode_level(int node_level)
	{
		this.node_level = node_level;
	}

	
}
